#include "push_swap.h"

int	lstsize(t_list *lst)
{
	int		i;
	t_list	*tmp;

	i = 0;
	tmp = lst;
	while (tmp)
	{
		i++;
		tmp = tmp->next;
		if (!tmp || tmp->head == true)
			break ;
	}
	return (i);
}
